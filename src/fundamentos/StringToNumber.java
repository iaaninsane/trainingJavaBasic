package fundamentos;

public class StringToNumber {

	public static void main(String[] args) {
		
		String palavra = "3";
		
		System.out.println(Integer.parseInt(palavra));
		System.out.println(Double.parseDouble(palavra));
		System.out.println(Float.parseFloat(palavra));
		System.out.println(Long.parseLong(palavra));
		

	}

}
